import socket as sk
import threading as mt

def openSocket(port):
    sock = sk.socket(sk.AF_INET, sk.SOCK_DGRAM)
    sock.setsockopt(sk.SOL_SOCKET, sk.SO_REUSEADDR, 1)
    sock.bind(("", port))
    print(f"Opened socket on port {port}")
    return sock

def command(socket, target, command, timeout = 5):
    socket.sendto(bytearray(command, 'ascii'), target)
    print("Command sent. Waiting for reply...")
    try:
        message, sender = socket.recvfrom(1024)
        print("Received: \n\t", message.decode('ascii'))
        print("From: ", str(sender))
    except sk.error:
        print("Timed out")

